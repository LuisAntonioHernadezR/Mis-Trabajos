public class Operaciones {
	/**
	 * 
	 * @param n1 se introduce valores enteros
	 * @param n2 se introduce valores enteros
	 * @return retorna la suma de los valores enteros
	 */
	public int sumar(int n1, int n2){
		return n1 + n2;
	}
/**
 * 	
 * @param n1 se introduce valores enteros
 * @param n2 se introduce valores enteros
 * @return retorna la resta de los valores enteros
 */
	public int restar(int n1, int n2){
		return n1 - n2;
	}
	/**
	 * 
	 * @param n1 se introduce valores enteros 
	 * @param n2 se introduce valores enteros
	 * @return retorna la multiplicacion de los valores enteros
	 */
	public int multiplicar(int n1, int n2){
		return n1 * n2;
	}
/**
 * 	
 * @param n1 se introduce valores enteros
 * @param n2 se introduce valores enteros
 * @return se divide los valores enteros
 */
	public int dividir(int n1, int n2){
		return n1 / n2;
	}
}
